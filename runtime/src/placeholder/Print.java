package placeholder;

/**
 * Provides very basic printing functions, until we can support System.out.*
 * The polyllvmc versions are implemented in a handwritten LLVM IR file.
 */
public class Print {

    public static void println(String s) {
        System.out.println(s);
    }

    public static void print(String s) {
        System.out.print(s);
    }

    public static void println(boolean n) {
        System.out.println(n);
    }

    public static void print(boolean n) {
        System.out.print(n);
    }

    public static void println(int n) {
        System.out.println(n);
    }

    public static void print(int n) {
        System.out.print(n);
    }

    public static void println(long n) {
        System.out.println(n);
    }

    public static void print(long n) {
        System.out.print(n);
    }

    public static void println(float n) {
        System.out.println(n);
    }

    public static void print(float n) {
        System.out.print(n);
    }

    public static void println(double n) {
        System.out.println(n);
    }

    public static void print(double n) {
        System.out.print(n);
    }
}
